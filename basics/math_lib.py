import math

x = math.sqrt(16)
print(x)  

result = math.pow(2, 3)
print(result)  

abs_value = abs(-10)
print(abs_value)  

ceil_value = math.ceil(4.3)
floor_value = math.floor(4.7)
print(ceil_value)  
print(floor_value)  


fact = math.factorial(5)
print(fact)  


gcd_value = math.gcd(48, 18)
print(gcd_value)  

# euler's number
exp_value = math.exp(1)
print(exp_value)

